class SecurityAnalystAI:
    """AI-powered security analysis, education, and remediation.

    Uses local Qwen for rich natural-language output and falls
    back to deterministic rule-based responses when the LLM is unavailable.

    Parameters
    ----------
    llm_client : LocalLLMClient | None
        Injected client; uses the singleton local client when ``None``.
    """

    def __init__(self, llm_client: LocalLLMClient | None = None) -> None:
        """Initialise the analyst with a local LLM client."""
        self.llm = llm_client or local_llm_client_singleton

    # ── METHOD 1: explain_threat ──────────────────────────────────────

    async def explain_threat(self, assessment: ThreatAssessment) -> ThreatExplanation:
        """Generate a structured explanation for a threat assessment.

        Sends the assessment details to the LLM with a carefully engineered
        prompt and parses the JSON response.  Falls back to rule-based
        output when the LLM is unavailable or returns unparseable content.

        Parameters
        ----------
        assessment : ThreatAssessment
            Output from :py:meth:`ThreatScorer.calculate`.

        Returns
        -------
        ThreatExplanation
            Structured explanation with title, explanation, technical
            detail, recommendations, and severity reasoning.
        """
        if not assessment.active_threats:
            return ThreatExplanation(
                title="All Clear",
                explanation="No security threats were detected on this device.",
                technical_detail="All RASP checks passed without triggering any threat signals.",
                recommendation=["Continue using the app normally.", "Keep your device updated."],
                severity_reason="No threats detected, risk score is zero.",
                source="rule_based",
            )

        prompt = (
            f"A mobile device reported the following security threats:\n"
            f"Active threats: {', '.join(assessment.active_threats)}\n"
            f"Risk score: {assessment.score}\n"
            f"Risk level: {assessment.level}\n\n"
            f"Provide a JSON response with exactly these keys:\n"
            f'- "title": short threat title (max 10 words)\n'
            f'- "explanation": plain English explanation (2-3 sentences, non-technical first)\n'
            f'- "technical_detail": technical explanation (2-3 sentences)\n'
            f'- "recommendation": list of 3-5 actionable steps the user should take\n'
            f'- "severity_reason": why this specific combination warrants the risk level\n\n'
            f"Respond ONLY with valid JSON. No markdown. No extra text."
        )

        try:
            raw = await self.llm.generate(prompt=prompt, system=_SYSTEM_THREAT_ANALYSIS, temperature=0.3)
            parsed = self._parse_json_response(raw)

            if parsed:
                return ThreatExplanation(
                    title=parsed.get("title", assessment.summary[:60]),
                    explanation=parsed.get("explanation", ""),
                    technical_detail=parsed.get("technical_detail", ""),
                    recommendation=parsed.get("recommendation", []),
                    severity_reason=parsed.get("severity_reason", ""),
                    source="llm",
                )
        except LocalLLMUnavailableError:
            logger.warning("Local LLM unavailable — using rule-based threat explanation")
        except Exception as exc:
            logger.error("Error during LLM threat explanation: %s", exc, exc_info=True)

        # ── Rule-based fallback ───────────────────────────────────────
        return self._rule_based_explanation(assessment)

    # ── METHOD 2: answer_security_question ────────────────────────────

    async def answer_security_question(
        self,
        question: str,
        context: dict[str, Any] | None = None,
    ) -> str:
        """Answer a user's security question using the LLM.

        Injects relevant topic context for known subjects (Frida, rooting,
        MITM, etc.) and falls back to a canned response when the local LLM is
        unavailable.

        Parameters
        ----------
        question : str
            The user's question text.
        context : dict | None
            Optional extra context (e.g. current device threat status).

        Returns
        -------
        str
            The AI-generated (or rule-based fallback) answer.
        """
        # ── Inject topic-specific context ─────────────────────────────
        extra_context = ""
        question_lower = question.lower()
        for keyword, ctx_text in _TOPIC_CONTEXT.items():
            if keyword in question_lower:
                extra_context += f"\n{ctx_text}"

        if context:
            extra_context += f"\nDevice context: {json.dumps(context, default=str)}"

        prompt = question
        if extra_context:
            prompt = f"{extra_context}\n\nUser question: {question}"

        try:
            return await asyncio.wait_for(
                self.llm.generate(
                    prompt=prompt,
                    system=_SYSTEM_SECURITY_EDUCATOR,
                    temperature=0.35,
                    max_tokens=192,
                ),
                timeout=_CHAT_TIMEOUT_SECONDS,
            )
        except LocalLLMUnavailableError:
            logger.warning("Local LLM unavailable — using rule-based chat response")
            return self._rule_based_answer(question)
        except asyncio.TimeoutError:
            logger.warning("Local LLM chat timed out — using rule-based chat response")
            return self._rule_based_answer(question)
        except Exception as exc:
            logger.error("Error during LLM chat: %s", exc, exc_info=True)
            return self._rule_based_answer(question)

    # ── METHOD 3: generate_remediation_plan ───────────────────────────

    async def generate_remediation_plan(self, threats: list[str]) -> RemediationPlan:
        """Generate a step-by-step remediation plan for active threats.

        Attempts LLM generation first, falls back to curated rule-based
        steps per threat type.

        Parameters
        ----------
        threats : list[str]
            Names of active threat signals.

        Returns
        -------
        RemediationPlan
            Ordered steps, time estimate, difficulty, and priority.
        """
        if not threats:
            return RemediationPlan(
                steps=[{
                    "step_number": 1,
                    "action": "No action needed",
                    "description": "No threats detected. Continue using the app normally.",
                    "difficulty": "easy",
                }],
                estimated_time="0 minutes",
                requires_technical_knowledge=False,
                priority="NORMAL",
            )

        prompt = (
            f"Generate a remediation plan for these mobile security threats: {', '.join(threats)}\n\n"
            f"Respond with a JSON object containing exactly these keys:\n"
            f'- "steps": list of objects with keys "step_number", "action", "description", "difficulty"\n'
            f'  (difficulty is one of: "easy", "moderate", "advanced")\n'
            f'- "estimated_time": string (e.g. "5 minutes")\n'
            f'- "requires_technical_knowledge": boolean\n'
            f'- "priority": one of "IMMEDIATE", "HIGH", "NORMAL"\n\n'
            f"Respond ONLY with valid JSON. No markdown. No extra text."
        )

        try:
            raw = await self.llm.generate(prompt=prompt, system=_SYSTEM_REMEDIATION, temperature=0.3)
            parsed = self._parse_json_response(raw)
            if parsed and "steps" in parsed:
                return RemediationPlan(
                    steps=parsed.get("steps", []),
                    estimated_time=parsed.get("estimated_time", "10 minutes"),
                    requires_technical_knowledge=parsed.get("requires_technical_knowledge", False),
                    priority=parsed.get("priority", "NORMAL"),
                )
        except LocalLLMUnavailableError:
            logger.warning("Local LLM unavailable — using rule-based remediation plan")
        except Exception as exc:
            logger.error("Error during LLM remediation plan: %s", exc, exc_info=True)

        return self._rule_based_remediation(threats)

    # ── Generate suggested follow-up questions ────────────────────────

    async def generate_follow_up_questions(
        self,
        user_message: str,
        ai_response: str,
    ) -> list[str]:
        """Generate three suggested follow-up questions.

        Attempts LLM generation; returns curated defaults on failure.

        Parameters
        ----------
        user_message : str
            The user's original question.
        ai_response : str
            The AI's answer to the question.

        Returns
        -------
        list[str]
            Three follow-up questions the user might ask next.
        """
        return [
            "How can I protect my device from this threat?",
            "What are the signs of a compromised device?",
            "Should I reinstall the app to be safe?",
        ]

    # ── Private helpers ───────────────────────────────────────────────

    @staticmethod
    def _parse_json_response(raw: str) -> Any:
        """Attempt to parse an LLM response as JSON.

        Handles common LLM quirks like wrapping in code fences.

        Parameters
        ----------
        raw : str
            Raw text from the LLM.

        Returns
        -------
        dict | list | None
            Parsed JSON or ``None`` on failure.
        """
        text = raw.strip()
        # Strip markdown code fences if present
        if text.startswith("```"):
            lines = text.split("\n")
            text = "\n".join(lines[1:])
            if text.endswith("```"):
                text = text[:-3]
            text = text.strip()

        try:
            return json.loads(text)
        except json.JSONDecodeError:
            # Try to find JSON object or array in the text
            for start_char, end_char in [("{", "}"), ("[", "]")]:
                start = text.find(start_char)
                end = text.rfind(end_char)
                if start != -1 and end != -1 and end > start:
                    try:
                        return json.loads(text[start : end + 1])
                    except json.JSONDecodeError:
                        continue
            logger.warning("Failed to parse LLM JSON response: %s", text[:200])
            return None

    @staticmethod
    def _rule_based_explanation(assessment: ThreatAssessment) -> ThreatExplanation:
        """Build a ThreatExplanation from curated threat descriptions.

        Parameters
        ----------
        assessment : ThreatAssessment
            The scored assessment to explain.

        Returns
        -------
        ThreatExplanation
            Deterministic, rule-based explanation.
        """
        explanations: list[str] = []
        technical_parts: list[str] = []
        recommendations: list[str] = []

        for threat in assessment.active_threats:
            info = _THREAT_DESCRIPTIONS.get(threat, {})
            if info:
                explanations.append(info.get("explanation", ""))
                technical_parts.append(info.get("technical", ""))
                recommendations.append(info.get("action", ""))

        # Build title from top threats
        labels = [
            _THREAT_DESCRIPTIONS.get(t, {}).get("label", t.replace("_", " ").title())
            for t in assessment.active_threats[:3]
        ]
        title = " + ".join(labels) + " Detected"

        return ThreatExplanation(
            title=title[:80],
            explanation=" ".join(explanations) if explanations else assessment.summary,
            technical_detail=" ".join(technical_parts) if technical_parts else "Multiple RASP checks triggered.",
            recommendation=recommendations if recommendations else ["Contact your IT security team."],
            severity_reason=(
                f"Combined risk score of {assessment.score} ({assessment.level}) indicates "
                f"{'critical security compromise requiring immediate attention' if assessment.level == 'CRITICAL' else 'elevated risk that should be addressed'}."
            ),
            source="rule_based",
        )

    @staticmethod
    def _rule_based_answer(question: str) -> str:
        """Provide a canned answer for common security questions.

        Parameters
        ----------
        question : str
            The user's question text.

        Returns
        -------
        str
            A rule-based answer string.
        """
        q = question.lower()
        if "frida" in q:
            return (
                "Frida is a security testing tool that can modify how apps work while they're running. "
                "Technically, it injects a JavaScript engine into app processes to intercept and alter "
                "function calls. If Frida is detected on your device, it could mean someone is trying "
                "to bypass the app's security protections. You should remove any Frida-related software "
                "from your device."
            )
        if "root" in q or "jailbreak" in q:
            return (
                "Rooting (Android) or jailbreaking (iOS) means giving yourself superuser access to "
                "your device's operating system. While this gives more control, it also removes "
                "important security barriers. Rooted devices are more vulnerable to malware and "
                "data theft because apps can access protected system files. For best security, "
                "use an unrooted device."
            )
        if "emulator" in q or "virtual" in q:
            return (
                "An emulator is software that mimics a physical phone on a computer. While useful "
                "for development, attackers use emulators to clone apps, automate fraudulent "
                "transactions, or reverse-engineer app logic. Our RASP system detects emulators "
                "to prevent these abuse scenarios."
            )
        if "mitm" in q or "man in the middle" in q or "proxy" in q:
            return (
                "A Man-in-the-Middle (MITM) attack happens when someone secretly intercepts the "
                "communication between your app and the server. Using proxy tools, an attacker can "
                "read, modify, or inject data into your network traffic. VPN and proxy detection "
                "helps identify potential MITM setups. Always use trusted networks and verify your "
                "network settings."
            )
        if "block" in q:
            return (
                "Your device may be blocked if our security system detected persistent or severe "
                "threats like rooting combined with instrumentation tools. Blocking protects your "
                "account from potential compromise. To regain access, remove any security threats "
                "and contact support."
            )
        return (
            "That's a great security question. While I can't provide a detailed AI-generated "
            "answer right now (the AI service is temporarily unavailable), I recommend consulting "
            "your organization's security team or visiting OWASP Mobile Security resources at "
            "https://owasp.org/www-project-mobile-security/ for comprehensive guidance."
        )

    @staticmethod
    def _rule_based_remediation(threats: list[str]) -> RemediationPlan:
        """Build a remediation plan from curated per-threat steps.

        Parameters
        ----------
        threats : list[str]
            List of active threat names.

        Returns
        -------
        RemediationPlan
            Deterministic remediation plan with ordered steps.
        """
        steps: list[dict[str, Any]] = []
        has_advanced = False
        step_num = 0

        for threat in threats:
            info = _THREAT_DESCRIPTIONS.get(threat, {})
            step_num += 1
            difficulty = "advanced" if threat in ("root_detected", "frida_detected", "hook_detected", "malware_detected") else "easy"
            if difficulty == "advanced":
                has_advanced = True
            steps.append({
                "step_number": step_num,
                "action": f"Address {info.get('label', threat.replace('_', ' '))}",
                "description": info.get("action", "Investigate and resolve this threat."),
                "difficulty": difficulty,
            })

        # Final verification step
        step_num += 1
        steps.append({
            "step_number": step_num,
            "action": "Verify Resolution",
            "description": "Restart the app and check if the security warnings are resolved.",
            "difficulty": "easy",
        })

        high_severity = any(t in ("root_detected", "frida_detected", "malware_detected", "hook_detected") for t in threats)

        return RemediationPlan(
            steps=steps,
            estimated_time=f"{len(threats) * 5} minutes",
            requires_technical_knowledge=has_advanced,
            priority="IMMEDIATE" if high_severity else ("HIGH" if len(threats) > 2 else "NORMAL"),
        )

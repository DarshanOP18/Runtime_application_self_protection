"""
app/ai/security_analyst.py
───────────────────────────
AI-powered security analysis, education, and remediation services.

Uses the local Ollama/Qwen client for threat explanations, security Q&A,
and remediation plans. Every method degrades gracefully to
rule-based fallbacks when the LLM is unavailable.

The AI NEVER helps bypass security — it only explains, educates, and
guides remediation.
"""


@dataclass
class ThreatExplanation:
    """Structured explanation of a threat assessment.

    Attributes
    ----------
    title : str
        Short headline (max ~10 words).
    explanation : str
        Plain-English, non-technical explanation.
    technical_detail : str
        Technical explanation for advanced users.
    recommendation : list[str]
        3-5 actionable remediation steps.
    severity_reason : str
        Why this specific combination warrants its risk level.
    source : str
        ``"llm"`` when generated by the local model, ``"rule_based"`` on fallback.
    """

    title: str = ""
    explanation: str = ""
    technical_detail: str = ""
    recommendation: list[str] = field(default_factory=list)
    severity_reason: str = ""
    source: str = "rule_based"


@dataclass
class RemediationStep:
    """A single step in a remediation plan.

    Attributes
    ----------
    step_number : int
        Ordinal position.
    action : str
        Short action label.
    description : str
        Full description of what to do.
    difficulty : str
        ``"easy"`` | ``"moderate"`` | ``"advanced"``.
    """

    step_number: int = 0
    action: str = ""
    description: str = ""
    difficulty: str = "easy"


@dataclass
class RemediationPlan:
    """Complete remediation plan for detected threats.

    Attributes
    ----------
    steps : list[dict]
        Ordered list of remediation step dictionaries.
    estimated_time : str
        Human-readable time estimate (e.g. "5 minutes").
    requires_technical_knowledge : bool
        Whether the steps need advanced user skills.
    priority : str
        ``"IMMEDIATE"`` | ``"HIGH"`` | ``"NORMAL"``.
    """

    steps: list[dict[str, Any]] = field(default_factory=list)
    estimated_time: str = "5 minutes"
    requires_technical_knowledge: bool = False
    priority: str = "NORMAL"


